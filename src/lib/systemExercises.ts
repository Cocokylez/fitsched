export function getAllowedEquipmentForEnvironment(environment?: string | null) {
  if (environment === "home_bodyweight") return ["BODYWEIGHT"]
  if (environment === "home_dumbbells") return ["BODYWEIGHT", "DUMBBELLS"]
  return null
}

export async function ensureSystemExercises(db: any) {
  type ExistingSystemExercise = {
    name: string
    muscleGroup: string
    equipment: string
    difficulty: string
    description: string | null
  }

  const existing = await db.exercise.findMany({
    where: { isSystem: true },
    select: { name: true, muscleGroup: true, equipment: true, difficulty: true, description: true },
  }) as ExistingSystemExercise[]
  const existingByName = new Map<string, ExistingSystemExercise>(existing.map((exercise) => [exercise.name.toLowerCase(), exercise]))
  const existingNames = new Set<string>(existingByName.keys())
  const missing = SYSTEM_EXERCISES.filter((exercise) => !existingNames.has(exercise.name.toLowerCase()))
  const stale = SYSTEM_EXERCISES.filter((exercise) => {
    const current = existingByName.get(exercise.name.toLowerCase())
    return current && (
      current.muscleGroup !== exercise.muscleGroup ||
      current.equipment !== exercise.equipment ||
      current.difficulty !== exercise.difficulty ||
      current.description !== exercise.description
    )
  })

  if (stale.length > 0) {
    await Promise.all(
      stale.map((exercise) =>
        db.exercise.updateMany({
          where: { isSystem: true, name: exercise.name },
          data: {
            muscleGroup: exercise.muscleGroup,
            equipment: exercise.equipment,
            difficulty: exercise.difficulty,
            description: exercise.description,
          },
        }),
      ),
    )
  }

  if (missing.length === 0) return

  await Promise.all(
    missing.map((exercise) =>
      db.exercise.create({
        data: {
          name: exercise.name,
          muscleGroup: exercise.muscleGroup,
          equipment: exercise.equipment,
          difficulty: exercise.difficulty,
          description: exercise.description,
          isSystem: true,
        },
      }),
    ),
  )
}
